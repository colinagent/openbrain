function resolveExecutionModelConfig(
  selectedModelKey: string | null,
  agentModelKey?: string | null,
): {
  effectiveModelKey: string | null;
  effectiveModel: ExecutionModel;
  errorMessage: string | null;
} {
  const normalizedSelectedModelKey = normalizeModelKey(selectedModelKey);
  const normalizedAgentModelKey = normalizeModelKey(agentModelKey);
  const modelsConfig = useModelsStore.getState().config;
  const enabledModels = modelsConfig.models.filter((model) => model.enabled);
  const resolveExplicit = (modelKey: string) => resolveChatModelSelection(enabledModels, modelKey);

  if (normalizedSelectedModelKey) {
    const modelSelection = resolveExplicit(normalizedSelectedModelKey);
    return {
      effectiveModelKey: modelSelection.effectiveModelKey,
      effectiveModel: modelSelection.effectiveModel,
      errorMessage: modelSelection.effectiveModel
        ? null
        : `Selected model "${normalizedSelectedModelKey}" is not available. Open Models and choose an enabled chat model.`,
    };
  }

  if (normalizedAgentModelKey) {
    const modelSelection = resolveExplicit(normalizedAgentModelKey);
    return {
      effectiveModelKey: modelSelection.effectiveModelKey,
      effectiveModel: modelSelection.effectiveModel,
      errorMessage: modelSelection.effectiveModel
        ? null
        : `AGENT.md model "${normalizedAgentModelKey}" is not available. Update the agent model field or enable/configure that model in Models settings.`,
    };
  }

  return {
    effectiveModelKey: null,
    effectiveModel: null,
    errorMessage: getNoModelAvailableMessage(),
  };
}

function resolveExecutionModelPreferenceMeta(
  effectiveModel: ExecutionModel,
  thinkingLevelOverride?: ThinkingLevel | null,
): ExecutionModelPreferenceMeta {
  const preference = resolveChatModelPreference(useModelsStore.getState().config, effectiveModel);
  const thinkingLevel = thinkingLevelOverride === undefined || thinkingLevelOverride === null
    ? preference.thinkingLevel
    : normalizeThinkingLevelForModel(effectiveModel, thinkingLevelOverride);

  return {
    thinkingLevel,
    ...(preference.contextWindow ? { contextWindow: preference.contextWindow } : {}),
    ...(preference.serviceTier === 'priority' ? { serviceTier: 'priority' as const } : {}),
  };
}

