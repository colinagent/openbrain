function extractText(event: GeneralContent): string {
  const content = event.content;
  if (!content) return '';
  if (typeof content.text === 'string') return content.text;
  if (typeof content.message === 'string') return content.message;
  if (typeof content.payload === 'string') return content.payload;
  return '';
}

function extractJsonPayload<T>(event: GeneralContent): T | null {
  const content = event.content;
  if (!content) {
    return null;
  }
  const payload = content.payload ?? content.message;
  if (!payload) {
    return null;
  }
  if (typeof payload === 'string') {
    try {
      return JSON.parse(payload) as T;
    } catch {
      return null;
    }
  }
  if (typeof payload === 'object') {
    return payload as T;
  }
  return null;
}

function parseMetaIndex(value: unknown): number {
  const parsed = Number.parseInt(String(value ?? ''), 10);
  return Number.isFinite(parsed) && parsed >= 0 ? parsed : -1;
}

function canonicalToolCallStreamKey(event: GeneralContent): string {
  const index = parseMetaIndex(event.meta?.contentIndex);
  if (index >= 0) {
    return `toolcall-index-${index}`;
  }
  const id = typeof event.meta?.id === 'string' ? event.meta.id.trim() : '';
  if (id) {
    return id;
  }
  return `toolcall-${Date.now()}`;
}

function normalizeToolCallPayload(event: GeneralContent): ToolCallPayload | null {
  const payload = extractJsonPayload<{ toolCall?: ToolCallPayload } & ToolCallPayload>(event);
  const toolCall = payload?.toolCall || payload;
  if (!toolCall || typeof toolCall !== 'object') {
    const metaID = typeof event.meta?.id === 'string' ? event.meta.id.trim() : '';
    const metaName = typeof event.meta?.name === 'string' ? event.meta.name.trim() : '';
    if (metaID || metaName) {
      return {
        ...(metaID ? { id: metaID } : {}),
        ...(metaName ? { name: metaName } : {}),
      };
    }
    return null;
  }
  const rawArguments = typeof toolCall.rawArguments === 'string' ? toolCall.rawArguments : '';
  const delta = typeof payload?.delta === 'string'
    ? payload.delta
    : (typeof toolCall.delta === 'string' ? toolCall.delta : '');
  const args = toolCall.arguments && typeof toolCall.arguments === 'object'
    ? toolCall.arguments
    : undefined;
  const id = typeof toolCall.id === 'string' && toolCall.id.trim()
    ? toolCall.id.trim()
    : (typeof event.meta?.id === 'string' ? event.meta.id.trim() : '');
  const name = typeof toolCall.name === 'string' && toolCall.name.trim()
    ? toolCall.name.trim()
    : (typeof event.meta?.name === 'string' ? event.meta.name.trim() : '');
  if (!id && !name && !delta && !rawArguments && !args) {
    return null;
  }
  return {
    ...(id ? { id } : {}),
    ...(name ? { name } : {}),
    ...(delta ? { delta } : {}),
    ...(rawArguments ? { rawArguments } : {}),
    ...(args ? { arguments: args } : {}),
    ...(toolCall.complete === true ? { complete: true } : {}),
  };
}

