function buildConversationChunkAppendText(currentContent: string, chunk: string): string {
  const normalizedChunk = chunk.trim();
  if (!normalizedChunk) {
    return '';
  }
  const normalizedCurrent = (currentContent || '').replace(/\r\n/g, '\n');
  if (!normalizedCurrent) {
    return normalizedChunk;
  }
  if (normalizedCurrent.endsWith('\n\n')) {
    return normalizedChunk;
  }
  if (normalizedCurrent.endsWith('\n')) {
    return `\n${normalizedChunk}`;
  }
  return `\n\n${normalizedChunk}`;
}

function appendOptimisticUserTranscriptChunk(
  workspaceTabId: string,
  chatPath: string,
  chunk: string,
): boolean {
  const normalizedPath = normalizePosixPath(chatPath);
  const normalizedChunk = chunk.trim();
  if (!normalizedPath || !normalizedChunk) {
    return false;
  }
  const ws = useAppStore.getStoreByTabId(workspaceTabId).getState();
  const existing = ws.documents.find((tab) => normalizePosixPath((tab.filePath || '').trim()) === normalizedPath);
  if (!existing) {
    return false;
  }
  const appendText = buildConversationChunkAppendText(existing.content || '', normalizedChunk);
  if (!appendText) {
    return false;
  }
  ws.appendToTab(normalizedPath, appendText);
  return true;
}

function normalizeActivityStatus(status: unknown, fallback: LiveStepStatus): LiveStepStatus {
  return status === 'done' || status === 'error' || status === 'running'
    ? status
    : fallback;
}

function parseTokenCount(value: unknown): number {
  const parsed = Number.parseInt(String(value ?? ''), 10);
  return Number.isFinite(parsed) && parsed >= 0 ? parsed : 0;
}

function parseMetaBoolean(value: unknown): boolean | null {
  if (typeof value === 'boolean') {
    return value;
  }
  const normalized = String(value ?? '').trim().toLowerCase();
  if (normalized === 'true') {
    return true;
  }
  if (normalized === 'false') {
    return false;
  }
  return null;
}

