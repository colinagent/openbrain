function threadTarget(threadID: string | null | undefined, chatPath: string | null | undefined): ConversationTarget {
  const normalizedThreadID = (threadID || '').trim();
  if (!normalizedThreadID) {
    return null;
  }
  const normalizedChatPath = normalizePosixPath((chatPath || '').trim());
  return {
    kind: 'thread',
    threadID: normalizedThreadID,
    ...(normalizedChatPath ? { chatPath: normalizedChatPath } : {}),
  };
}

function commandTarget(chatPath: string | null | undefined): ConversationTarget {
  const normalizedPath = normalizePosixPath((chatPath || '').trim());
  return normalizedPath ? { kind: 'command', path: normalizedPath } : null;
}

