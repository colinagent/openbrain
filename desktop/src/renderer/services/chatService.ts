async function resolveBoundPlanFileForTurn(
  workspaceTabId: string,
  params: { planPath: string }
): Promise<VerifiedPlanFileResult> {
  const planPath = normalizePosixPath((params.planPath || '').trim());
  const wsStore = useAppStore.getStoreByTabId(workspaceTabId);
  const stat = planPath
    ? await wsStore.getState().statPath(planPath)
    : { error: 'missing path', isDir: false };
  const content = !stat.error && !stat.isDir && planPath
    ? await wsStore.getState().readTextFile(planPath)
    : null;
  return verifyBoundPlanFileSnapshot({
    snapshot: {
      path: planPath,
      exists: !stat.error,
      isDir: Boolean(stat.isDir),
      content,
    },
  });
}

async function ensureBoundPlanDirectoryForThread(
  workspaceTabId: string,
  planDirOrPath: string,
): Promise<void> {
  const normalizedPlanDirOrPath = normalizePosixPath((planDirOrPath || '').trim());
  if (!normalizedPlanDirOrPath) {
    throw new Error('Plan turn requires a usable plan directory.');
  }
  const wsStore = useAppStore.getStoreByTabId(workspaceTabId);
  const planDir = normalizedPlanDirOrPath.endsWith('.md')
    ? getParentDir(normalizedPlanDirOrPath)
    : normalizedPlanDirOrPath;
  if (!planDir) {
    throw new Error(`无法解析 plan 目录：${normalizedPlanDirOrPath}`);
  }
  await wsStore.getState().ensureDirectory(planDir);
  const stat = await wsStore.getState().statPath(planDir);
  if (stat.error || !stat.isDir) {
    throw new Error(`创建 plan 目录失败：${planDir}`);
  }
}

