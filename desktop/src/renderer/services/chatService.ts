async function persistLocalChatFrontmatter(
  workspaceTabId: string,
  chatPath: string,
  patch: { title?: string | null },
): Promise<void> {
  const normalizedChatPath = normalizePosixPath((chatPath || '').trim());
  if (!normalizedChatPath) {
    return;
  }

  const wsStore = useAppStore.getStoreByTabId(workspaceTabId);
  const state = wsStore.getState();
  const openTab = state.documents.find(
    (tab) => tab.documentRole === 'conversation'
      && normalizePosixPath((tab.filePath || '').trim()) === normalizedChatPath,
  );
  let currentContent = openTab?.content ?? null;
  if (currentContent == null) {
    currentContent = await state.readTextFile(normalizedChatPath);
  }
  if (currentContent == null) {
    return;
  }

  const nextContent = updateChatFrontmatter(currentContent, {
    ...(Object.prototype.hasOwnProperty.call(patch, 'title') ? { title: patch.title || null } : {}),
  });
  if (nextContent === currentContent) {
    return;
  }

  const written = await wsStore.getState().writeTextFile(normalizedChatPath, nextContent);
  if (!written) {
    throw new Error('Persist chat frontmatter failed.');
  }

  await wsStore.getState().reloadOpenTabsByPaths([normalizedChatPath], { skipDirty: false });
  primeLocalThreadMeta(normalizedChatPath, workspaceTabId);
}

function isAutoGeneratedChatPath(chatPath: string | null | undefined): boolean {
  const fileName = normalizePosixPath((chatPath || '').trim()).split('/').pop() || '';
  const stem = fileName.replace(/\.md$/i, '').trim().toLowerCase();
  return /^(untitled-chat|image)(?:-\d+)?$/.test(stem);
}

export function resolveAutoRetitleTitle(
  threadMeta: Pick<ThreadMeta, 'chatPath' | 'path' | 'title'> | null | undefined,
  nextTitle: string,
): string {
  if (!threadMeta || !isAutoGeneratedChatPath(threadMeta.chatPath || threadMeta.path)) {
    return '';
  }
  const currentTitle = (threadMeta.title || '').trim();
  if (currentTitle && !isAutoGeneratedChatTitle(currentTitle)) {
    return currentTitle;
  }
  const normalizedTitle = (nextTitle || '').trim();
  if (!normalizedTitle || isAutoGeneratedChatTitle(normalizedTitle)) {
    return '';
  }
  return normalizedTitle;
}

