async function runPreparedPlanExecution(options: PlanExecutionRunOptions): Promise<void> {
  const workspaceTabId = options.workspaceTabId;
  const chatState = getWorkspaceChatState(workspaceTabId);
  const wsStore = useAppStore.getStoreByTabId(workspaceTabId);
  let ws = wsStore.getState();
  if (!requireSignedIn('plan')) {
    return;
  }
  if (!(await ensureChatAccessOrPrompt('plan'))) {
    return;
  }
  if (ws.connectionState && ws.connectionState !== 'connected') {
    chatState.setError('Not connected to server');
    return;
  }

  let chatPathToStream = normalizePosixPath(options.chatPath);
  if (!chatPathToStream) {
    chatState.setError('Plan execution requires an existing chat thread.');
    return;
  }
  try {
    chatPathToStream = await resolveCurrentChatPath(chatPathToStream, workspaceTabId);
  } catch {
    // Best effort path retarget.
  }
  const saved = await wsStore.getState().saveTabByPath(chatPathToStream);
  if (!saved) {
    chatState.setError('保存对话文件失败，请先保存后再执行。');
    return;
  }
  ws = wsStore.getState();

  await ensureChatModelsLoaded();
  const { effectiveModelKey, effectiveModel, errorMessage } = resolveExecutionModelConfig(
    options.selectedModelKey,
    options.agentTarget.agentModelKey,
  );
  if (!effectiveModelKey) {
    chatState.setError(errorMessage || getNoModelAvailableMessage());
    return;
  }
  if (errorMessage) {
    chatState.setError(errorMessage);
    return;
  }
  if (!await ensureCurrentModelAccess({
    effectiveModelKey,
    effectiveModel,
    loginReason: 'plan',
    onDenied: (message) => chatState.setErrorForChatPath(chatPathToStream, message),
  })) {
    return;
  }

  const threadMeta = await resolveRuntimeThreadMeta(workspaceTabId, {
    chatPath: chatPathToStream,
  });
  chatPathToStream = threadMeta.chatPath;
  const abortController = new AbortController();
  let streamTarget = targetForThreadMeta(threadMeta);
  setTargetAbortController(workspaceTabId, streamTarget, abortController);

  clearAwaitingUserForNewRun(chatState, chatPathToStream);
  chatState.prepareLiveOverlayForNewRun(chatPathToStream);
  chatState.setActivityExpanded(chatPathToStream, true);
  chatState.bumpPlanRevision(chatPathToStream);
  chatState.setTargetInProgress(streamTarget, true);
  chatState.setErrorForChatPath(chatPathToStream, null);
  const chatCapableOpCode = ws.getAgentOpCode?.(options.agentTarget.agentID) ?? '';
  if (!chatCapableOpCode) {
    chatState.setTargetInProgress(streamTarget, false);
    chatState.setErrorForChatPath(chatPathToStream, getAgentSelectionError(options.agentTarget.agentID));
    stopTargetStream(workspaceTabId, streamTarget, chatPathToStream);
    return;
  }

  let streamEndedPath = '';
  let streamAborted = false;
  let threadID = threadMeta.threadID;
  const executionModelMeta = resolveExecutionModelPreferenceMeta(effectiveModel, options.thinkingLevel);
  try {
    streamTarget = targetForThreadMeta(threadMeta);
    const fileID = (threadMeta.fileID || '').trim();
    scheduleChatTabReload(workspaceTabId, chatPathToStream, 120);
    scheduleChatTabReload(workspaceTabId, chatPathToStream, 120);
    const selectedSkillContext = resolveSelectedSkillContext(options.skill, {
      planPath: options.planPath,
    });
    const turnRequestID = generateTurnRequestID();
    await streamChatWithReconnect({
      url: `${resolveChatBaseUrl(workspaceTabId)}/v1/chat/stream`,
      payload: {
        meta: {
          ...executionModelMeta,
          ...buildSelectedSkillMeta(options.skill, selectedSkillContext),
          cwd: options.agentTarget.agentCwd,
          modelKey: effectiveModelKey,
          threadID,
          fileID,
          path: chatPathToStream,
          chatPath: chatPathToStream,
          turnRequestID,
          agentID: options.agentTarget.agentID,
          opcode: THREAD_SUBMIT_OPCODE,
          agentName: options.agentTarget.agentName,
          agentAvatar: options.agentTarget.agentAvatar || '',
        },
        content: buildChatUserContentPayload(options.text),
      },
      signal: abortController.signal,
      onEvent: (event) => {
        const endedPath = handleSSEEvent(workspaceTabId, event, chatPathToStream);
        if (endedPath) {
          streamEndedPath = endedPath;
        }
      },
      onReconnectStateChange: (state) => {
        if (state) {
          chatState.setReconnectState(chatPathToStream, state.attempt, state.limit, state.message);
        } else {
          chatState.clearReconnectState(chatPathToStream);
        }
      },
    });
  } catch (err) {
    if (isSignalAbortError(err, abortController.signal) || isContextCanceledError(err)) {
      streamAborted = true;
      return;
    }
    if (isLoginRequiredError(err)) {
      requestLogin('plan');
      return;
    }
    const msg = err instanceof Error ? err.message : 'Plan execution failed';
    const billingInfo = requestBillingReminderForError(msg, getActivityErrorCode(err));
    chatState.setErrorForChatPath(chatPathToStream, msg, getActivityErrorCode(err));
    chatState.pushLiveStep(chatPathToStream, {
      id: `err-${Date.now()}`,
      type: 'error',
      label: billingInfo?.summary || msg,
      status: 'error',
      ts: Date.now(),
    });
  } finally {
    const finishedPath = streamEndedPath || chatPathToStream;
    if (streamAborted && finishedPath) {
      finishAbortedActivityForChatPath(chatState, workspaceTabId, finishedPath);
    }
    const controllerKey = controllerKeyForChatPath(workspaceTabId, chatPathToStream);
    if (controllerKey && abortControllers.get(controllerKey) === abortController) {
      abortControllers.delete(controllerKey);
    }
    chatState.setChatPathInProgress(chatPathToStream, false);
    if (!streamAborted) {
      if (finishedPath) {
        clearScheduledChatReload(workspaceTabId, finishedPath);
        await wsStore.getState().reloadOpenTabsByPaths([finishedPath], { skipDirty: true });
        await refreshThreadReviews(workspaceTabId, finishedPath);
        await refreshThreadStateBestEffort(workspaceTabId, finishedPath);
        chatState.bumpPlanRevision(finishedPath);
      }
    }
  }
}

