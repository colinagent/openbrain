  const workspaceTabId = getActiveWorkspaceTabId();
  const normalized = normalizePlanBuildRequest(request);
  const chatState = getWorkspaceChatState(workspaceTabId);
  const wsStore = useAppStore.getStoreByTabId(workspaceTabId);
  const resolvedAgent = wsStore.getState().resolveAgentByID?.(normalized.agentID);
