async function ensureCurrentModelAccess(options: {
  effectiveModelKey: string | null;
  effectiveModel: ReturnType<typeof resolveChatModelSelection>['effectiveModel'];
  loginReason: LoginRequiredReason;
  onDenied: (message: string) => void;
}): Promise<boolean> {
  const denial = await getChatModelAccess(options.effectiveModelKey, options.effectiveModel);
  if (!denial) {
    return true;
  }
  if (denial.authInvalid) {
    requestLogin(options.loginReason);
    return false;
  }
  options.onDenied(denial.message);
  useBillingReminderStore.getState().show(denial.reminderKind || 'chat-unavailable');
  return false;
}

