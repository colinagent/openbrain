export async function stopStream(): Promise<void> {
  const workspaceTabId = getActiveWorkspaceTabId();
  const chatState = getWorkspaceChatState(workspaceTabId);
  const selectedTarget = chatState.selectedConversationTarget;
  if (!selectedTarget || !chatState.isTargetInProgress(selectedTarget)) {
    return;
  }
  if (selectedTarget.kind !== 'thread') {
    stopTargetStream(workspaceTabId, selectedTarget);
    chatState.setTargetInProgress(selectedTarget, false);
    return;
  }
  const streamingChatPath = getTargetChatPath(chatState, selectedTarget);
  const streamingThreadID = selectedTarget.threadID.trim();
  const streamingStateKey = streamingChatPath || streamingThreadID;

  try {
    const threadMeta = await resolveControlThreadMeta(workspaceTabId, streamingChatPath
      ? { chatPath: streamingChatPath, threadID: streamingThreadID }
      : { threadID: streamingThreadID });
    const threadKey = threadStateKey(threadMeta);
    const ack = await postChatControl(workspaceTabId, {
      meta: {
        opcode: 'thread/interrupted',
        threadID: threadMeta.threadID,
        fileID: threadMeta.fileID,
        path: threadMeta.path || threadMeta.chatPath,
        chatPath: threadMeta.chatPath,
        agentID: threadMeta.agentID,
      },
    });
    stopTargetStream(workspaceTabId, selectedTarget, threadKey);
    finishAbortedActivityForChatPath(chatState, workspaceTabId, threadKey);
    chatState.setErrorForTarget(selectedTarget, 'Aborted');
    const restoredQueued = await queuedMessagesFromAck(workspaceTabId, threadKey, ack.queuedMessages);
    chatState.clearQueuedMessages(threadKey);
    for (const queuedMessage of [...restoredQueued.steering, ...restoredQueued.followUp]) {
      if (threadMeta.chatPath) {
        restoreQueuedMessageToComposer(workspaceTabId, {
          ...queuedMessage,
          chatPath: threadMeta.chatPath,
        });
      } else {
        const currentDraft = chatState.getDraftForTarget(selectedTarget);
        chatState.setDraftForTarget(selectedTarget, mergeDraftText(currentDraft, queuedMessage.text));
        if (!chatState.selectedSkill && queuedMessage.selectedSkill) {
          chatState.setSelectedSkill(queuedMessage.selectedSkill);
        }
      }
    }
    chatState.setTargetInProgress(selectedTarget, false);
    if (threadMeta.chatPath) {
      await refreshThreadReviews(workspaceTabId, threadMeta.chatPath);
      await refreshThreadStateBestEffort(workspaceTabId, threadMeta.chatPath);
    } else {
      await refreshThreadStateByThreadID(threadMeta.threadID, {}, workspaceTabId).catch(() => null);
    }
  } catch (error) {
    if (isLoginRequiredError(error)) {
      requestLogin('thread-control');
      return;
    }
    if (isThreadNotRunningError(error)) {
      stopTargetStream(workspaceTabId, selectedTarget, streamingStateKey);
      finishAbortedActivityForChatPath(chatState, workspaceTabId, streamingStateKey);
      chatState.setTargetInProgress(selectedTarget, false);
      if (streamingChatPath) {
        await refreshThreadStateBestEffort(workspaceTabId, streamingChatPath);
      } else if (streamingThreadID) {
        await refreshThreadStateByThreadID(streamingThreadID, {}, workspaceTabId).catch(() => null);
      }
      return;
    }
    chatState.setErrorForTarget(selectedTarget, error instanceof Error ? error.message : 'Failed to interrupt remote run.');
  }
}

export async function stopCommand(): Promise<void> {
  const workspaceTabId = getActiveWorkspaceTabId();
  const wsStore = useAppStore.getStoreByTabId(workspaceTabId);
  const chatState = getWorkspaceChatState(workspaceTabId);
  const commandID = chatState.activeCommand?.commandID || '';
  if (!commandID) {
    const activeCommandPath = chatState.activeCommand?.filePath || '';
    chatState.setActiveCommand(null);
    chatState.setChatPathInProgress(activeCommandPath, false);
    return;
  }
  try {
    await wsStore.getState().stopCommand(commandID);
  } catch (error) {
    chatState.setErrorForChatPath(
      chatState.activeCommand?.filePath || '',
      error instanceof Error ? error.message : 'Failed to stop command.'
    );
  }
}

export function disposeChatWorkspaceRuntime(workspaceTabId: string): void {
  stopAllWorkspaceStreams(workspaceTabId);
  const chatState = getWorkspaceChatState(workspaceTabId);
  chatState.clearAllQueuedMessages();
  chatState.setActiveCommand(null);
  chatState.clearAllInProgress();
  chatState.clearAllAwaitingUsers();
  clearScheduledChatReload(workspaceTabId);
}
