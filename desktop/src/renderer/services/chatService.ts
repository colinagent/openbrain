async function sendCommandMessage(): Promise<void> {
  const workspaceTabId = getActiveWorkspaceTabId();
  const chatState = getWorkspaceChatState(workspaceTabId);
  const wsStore = useAppStore.getStoreByTabId(workspaceTabId);
  const selectedTarget = chatState.selectedConversationTarget;
  const initialTarget = selectedTarget;
  const rawText = chatState.getDraftForTarget(selectedTarget);
  const command = rawText.trim();
  if (!command) {
    return;
  }
  if (!requireSignedIn('command')) {
    return;
  }

  const ws = wsStore.getState();
  if (ws.connectionState && ws.connectionState !== 'connected') {
    chatState.setErrorForTarget(initialTarget, 'Not connected to server');
    return;
  }

  const workspaceRoot = (ws.currentDir || '').trim();
  if (!workspaceRoot) {
    chatState.setErrorForTarget(initialTarget, 'Workspace root is not available.');
    return;
  }

  const selectedPath = selectedTarget?.kind === 'command' ? selectedTarget.path : '';
  const targetPath = isCommandLogPath(selectedPath, workspaceRoot) ? selectedPath : '';
  const pendingConversationId = selectedTarget?.kind === 'pending' ? selectedTarget.id : null;

  chatState.setTargetInProgress(initialTarget, true);
  chatState.setErrorForTarget(initialTarget, null);

  try {
    const result = await ws.execCommand({
      command,
      workspaceRoot,
      ...(targetPath ? { targetPath } : {}),
    });
    void ws.openThreadTab(result.filePath);
    await ws.reloadOpenTabsByPaths([result.filePath], { skipDirty: true });
    if (pendingConversationId) {
      chatState.consumePendingConversation(pendingConversationId, result.filePath);
    } else {
      if (initialTarget?.kind === 'command' && initialTarget.path !== result.filePath) {
        chatState.setTargetInProgress(initialTarget, false);
      }
      chatState.setChatPathInProgress(result.filePath, true);
      chatState.selectChatConversation(result.filePath);
    }
    chatState.setDraftForSelectedTarget('');
    if (getWorkspaceChatState(workspaceTabId).isTargetInProgress(commandTarget(result.filePath))) {
      chatState.setActiveCommand({
        commandID: result.commandID,
        filePath: result.filePath,
      });
    }
  } catch (error) {
    chatState.setTargetInProgress(initialTarget, false);
    if (isLoginRequiredError(error)) {
      requestLogin('command');
      return;
    }
    chatState.setErrorForTarget(initialTarget, error instanceof Error ? error.message : 'Run command failed');
  }
}

type SubmitChatTurnOptions = {
  selectedSkillOverride?: SelectedSkill | null;
};

type SubmitIdleChatTurnOptions = SubmitChatTurnOptions & {
  continueChatPath?: string | null;
  continueThreadID?: string | null;
};

export async function submitChatTurn(options?: SubmitChatTurnOptions): Promise<void> {
  const workspaceTabId = getActiveWorkspaceTabId();
  const chatState = getWorkspaceChatState(workspaceTabId);
  const selectedTarget = chatState.selectedConversationTarget;
  const rawText = chatState.getDraftForTarget(selectedTarget);
  const selectedSkill = options?.selectedSkillOverride !== undefined
    ? normalizeSelectedSkill(options.selectedSkillOverride)
    : normalizeSelectedSkill(chatState.selectedSkill);
  const submitIntent = resolveConversationSubmitIntent({
    isCommandMode: chatState.inputMode === 'command',
    isSelectedTargetInProgress: Boolean(selectedTarget && chatState.isTargetInProgress(selectedTarget)),
    hasSubmissionContent: hasTurnSubmissionContent(rawText, selectedSkill),
    canContinueSelectedThread: canContinueIdleChatTarget(chatState, selectedTarget),
  });

  switch (submitIntent) {
    case 'noop':
      return;
    case 'command':
      await sendCommandMessage();
      return;
    case 'queue_steering':
      await queueSteering({ selectedSkillOverride: options?.selectedSkillOverride });
      return;
    case 'continue_thread':
      if (!selectedTarget || selectedTarget.kind !== 'thread') {
        return;
      }
      await submitIdleChatTurn({
        continueChatPath: getTargetChatPath(chatState, selectedTarget),
        continueThreadID: selectedTarget.threadID,
      });
      return;
    case 'submit_prompt':
      await submitIdleChatTurn(options);
      return;
    default:
      return;
  }
}

async function submitIdleChatTurn(options?: SubmitIdleChatTurnOptions): Promise<void> {
  const workspaceTabId = getActiveWorkspaceTabId();
  const chatState = getWorkspaceChatState(workspaceTabId);
  const wsStore = useAppStore.getStoreByTabId(workspaceTabId);
  const selectedTarget = chatState.selectedConversationTarget;
  const initialTarget = selectedTarget;
  const initialTargetKey = getConversationTargetKey(initialTarget);
  const rawText = chatState.getDraftForTarget(selectedTarget);
  const selectedSkill = options?.selectedSkillOverride !== undefined
    ? normalizeSelectedSkill(options.selectedSkillOverride)
    : normalizeSelectedSkill(chatState.selectedSkill);
  const continueChatPath = normalizePosixPath((options?.continueChatPath || '').trim());
  const continueThreadID = (options?.continueThreadID || '').trim();
  const selectedThreadID = selectedTarget?.kind === 'thread' ? selectedTarget.threadID.trim() : '';
  const isContinuationSubmit = Boolean(continueChatPath || continueThreadID);
  const hasSubmissionContent = hasTurnSubmissionContent(rawText, selectedSkill);
  const submissionText = buildSubmissionUserText(rawText, selectedSkill);
  const restoreSubmittedDraft = (target: ConversationTarget): void => {
    if (isContinuationSubmit || !target) {
      return;
    }
    const latestState = getWorkspaceChatState(workspaceTabId);
    if (latestState.getDraftForTarget(target)) {
      return;
    }
    latestState.setDraftForTarget(target, rawText);
    const targetKey = getConversationTargetKey(target);
    const selectedTargetKey = getConversationTargetKey(latestState.selectedConversationTarget);
    if (selectedSkill && targetKey && selectedTargetKey === targetKey && !latestState.selectedSkill) {
      latestState.setSelectedSkill(selectedSkill);
    }
  };
  if (!hasSubmissionContent && !isContinuationSubmit) {
    return;
  }

  if (!requireSignedIn('chat')) {
    return;
  }

  let ws = wsStore.getState();
  if (ws.connectionState && ws.connectionState !== 'connected') {
    chatState.setErrorForTarget(initialTarget, 'Not connected to server');
    return;
  }

  const abortController = new AbortController();
  const pendingConversationId = isContinuationSubmit
    ? null
    : (selectedTarget?.kind === 'pending' ? selectedTarget.id : null);
  let runningTarget: ConversationTarget = initialTarget;
  const rollbackSubmittedRun = (target: ConversationTarget = runningTarget, chatPath?: string | null): void => {
    const rollbackTarget = target || runningTarget || initialTarget;
    chatState.setTargetInProgress(rollbackTarget, false);
    restoreSubmittedDraft(rollbackTarget);
    stopTargetStream(workspaceTabId, rollbackTarget, chatPath);
  };

  if (runningTarget) {
    chatState.setTargetInProgress(runningTarget, true);
    setTargetAbortController(workspaceTabId, runningTarget, abortController);
  }
  if (!isContinuationSubmit) {
    chatState.setDraftForTarget(initialTarget, '');
    if (selectedSkill) {
      chatState.clearSelectedSkill();
    }
  }
  chatState.setErrorForTarget(initialTarget, null);

  let chatAccessAllowed = false;
  try {
    chatAccessAllowed = await ensureChatAccessOrPrompt('chat');
  } catch (error) {
    if (!abortController.signal.aborted) {
      rollbackSubmittedRun();
    }
    throw error;
  }
  if (abortController.signal.aborted) {
    return;
  }
  if (!chatAccessAllowed) {
    rollbackSubmittedRun();
    return;
  }

  let chatPathToStream = continueChatPath || (
    selectedTarget?.kind === 'thread'
      ? getTargetChatPath(chatState, selectedTarget)
      : ''
  );
  let threadID = continueThreadID || selectedThreadID;
  let freshThreadMeta: ThreadMeta | null = null;
  let threadOnlyThreadMeta: ThreadMeta | null = null;
  if (chatPathToStream) {
    try {
      chatPathToStream = await resolveCurrentChatPath(chatPathToStream, workspaceTabId);
    } catch {
      // Fall through to the existing save/open validation path.
    }
    if (abortController.signal.aborted) {
      return;
    }
  }
  if (!chatPathToStream && threadID) {
    try {
      threadOnlyThreadMeta = await resolveRuntimeThreadMeta(workspaceTabId, { threadID });
      threadID = threadOnlyThreadMeta.threadID || threadID;
    } catch (error) {
      if (!abortController.signal.aborted) {
        chatState.setErrorForTarget(initialTarget, error instanceof Error ? error.message : 'Load thread failed');
        rollbackSubmittedRun();
      }
      return;
    }
    if (abortController.signal.aborted) {
      return;
    }
  }
  if (chatPathToStream) {
    let saved = false;
    try {
      saved = await wsStore.getState().saveTabByPath(chatPathToStream);
    } catch (error) {
      if (!abortController.signal.aborted) {
        chatState.setErrorForTarget(initialTarget, error instanceof Error ? error.message : '保存对话文件失败。');
        rollbackSubmittedRun();
      }
      return;
    }
    if (abortController.signal.aborted) {
      return;
    }
    if (!saved) {
      chatState.setErrorForTarget(initialTarget, isContinuationSubmit ? '保存对话文件失败，请先保存后再继续。' : '保存对话文件失败，请先保存后再发送。');
      rollbackSubmittedRun();
      return;
    }
    ws = wsStore.getState();
  }

  const target = resolveTurnAgentTarget(chatState, ws, selectedTarget, chatPathToStream)
    || agentTargetFromThreadMeta(threadOnlyThreadMeta);
  const agentCwd = (target?.agentCwd || '').trim();
  const agentID = (target?.agentID || '').trim();
  const resolvedAgent = ws.resolveAgentByID?.(agentID);
  const agentName = (resolvedAgent?.name || '').trim();
  const agentAvatar = (resolvedAgent?.avatar || '').trim();
  const selectedModelKey = chatState.getSelectedModelKey();
  try {
    await ensureChatModelsLoaded();
  } catch (error) {
    if (!abortController.signal.aborted) {
      chatState.setErrorForTarget(initialTarget, error instanceof Error ? error.message : 'Load models failed');
      rollbackSubmittedRun();
    }
    return;
  }
  if (abortController.signal.aborted) {
    return;
  }
  const { effectiveModelKey, effectiveModel, errorMessage } = resolveExecutionModelConfig(
    selectedModelKey,
    resolvedAgent?.model || null,
  );
  if (!effectiveModelKey) {
    chatState.setErrorForTarget(initialTarget, errorMessage || getNoModelAvailableMessage());
    rollbackSubmittedRun();
    return;
  }
  if (errorMessage) {
    chatState.setErrorForTarget(initialTarget, errorMessage);
    rollbackSubmittedRun();
    return;
  }
  let currentModelAllowed = false;
  try {
    currentModelAllowed = await ensureCurrentModelAccess({
      effectiveModelKey,
      effectiveModel,
      loginReason: 'chat',
      onDenied: (message) => chatState.setErrorForTarget(initialTarget, message),
    });
  } catch (error) {
    if (!abortController.signal.aborted) {
      chatState.setErrorForTarget(initialTarget, error instanceof Error ? error.message : 'Check model access failed');
      rollbackSubmittedRun();
    }
    return;
  }
  if (abortController.signal.aborted) {
    return;
  }
  if (!currentModelAllowed) {
    rollbackSubmittedRun();
    return;
  }

  if (chatPathToStream) {
    const chatTarget = targetForChatPath(chatState, chatPathToStream);
    if (runningTarget && getConversationTargetKey(runningTarget) !== getConversationTargetKey(chatTarget)) {
      moveTargetAbortController(workspaceTabId, runningTarget, chatTarget);
      chatState.setTargetInProgress(runningTarget, false);
    } else if (!runningTarget) {
      setTargetAbortController(workspaceTabId, chatTarget, abortController);
    }
    runningTarget = chatTarget;
    clearAwaitingUserForNewRun(chatState, chatPathToStream);
    chatState.patchThreadSnapshotState(chatPathToStream, { runStatus: 'running' });
    chatState.setTargetInProgress(runningTarget, true);
    chatState.requestChatScrollToBottom(chatPathToStream);
    setTargetAbortController(workspaceTabId, runningTarget, abortController);
  } else if (threadOnlyThreadMeta) {
    const threadTargetForRun = targetForThreadMeta(threadOnlyThreadMeta);
    if (runningTarget && getConversationTargetKey(runningTarget) !== getConversationTargetKey(threadTargetForRun)) {
      moveTargetAbortController(workspaceTabId, runningTarget, threadTargetForRun);
      chatState.setTargetInProgress(runningTarget, false);
    } else if (!runningTarget) {
      setTargetAbortController(workspaceTabId, threadTargetForRun, abortController);
    }
    runningTarget = threadTargetForRun;
    clearAwaitingUserForNewRun(chatState, threadOnlyThreadMeta.threadID);
    chatState.patchThreadSnapshotState(threadOnlyThreadMeta.threadID, { runStatus: 'running' });
    chatState.setTargetInProgress(runningTarget, true);
    chatState.requestChatScrollToBottom(threadOnlyThreadMeta.threadID);
    setTargetAbortController(workspaceTabId, runningTarget, abortController);
  }

  if (!chatPathToStream && !threadOnlyThreadMeta) {
    if (isContinuationSubmit) {
      throw new Error('Chat path is required.');
    }
    if (!agentID) {
      const msg = getAgentSelectionError(agentID);
      chatState.setErrorForTarget(initialTarget, msg);
      rollbackSubmittedRun();
      return;
    }
    const createOpcode = ws.getAgentOpCode?.(agentID) ?? '';
    if (!createOpcode) {
      const msg = getAgentSelectionError(agentID);
      chatState.setErrorForTarget(initialTarget, msg);
      rollbackSubmittedRun();
      return;
    }
    try {
      const created = await createChat(
        agentCwd,
        buildSubmissionTitleSeed(rawText, selectedSkill),
        agentID,
        workspaceTabId,
        abortController.signal,
      );
      if (abortController.signal.aborted) {
        return;
      }
      const createdPath = normalizePosixPath(created.path || created.chatPath || '');
      chatPathToStream = createdPath;
      threadID = created.threadID.trim();
      freshThreadMeta = {
        threadID,
        fileID: (created.fileID || '').trim(),
        agentID,
        cwd: (created.cwd || agentCwd || '').trim(),
        path: createdPath,
        chatPath: createdPath,
        title: (created.title || '').trim(),
      };
      chatState.upsertThreadMeta(freshThreadMeta);
      const createdTarget = targetForThreadMeta(freshThreadMeta);
      moveTargetAbortController(workspaceTabId, runningTarget, createdTarget);
      ensureCreatedChatTab(workspaceTabId, createdPath, created.title, created.initialContent);
      if (pendingConversationId) {
        chatState.consumePendingConversation(pendingConversationId, createdPath, threadID);
      } else {
        if (getConversationTargetKey(runningTarget) !== getConversationTargetKey(createdTarget)) {
          chatState.setTargetInProgress(runningTarget, false);
        }
        chatState.setTargetInProgress(createdTarget, true);
        chatState.selectThreadConversation(threadID, createdPath);
      }
      runningTarget = createdTarget;
      chatState.requestChatScrollToBottom(createdPath);
      ws = wsStore.getState();
    } catch (err) {
      if (isSignalAbortError(err, abortController.signal)) {
        return;
      }
      const msg = err instanceof Error ? err.message : 'Create chat failed';
      const billingInfo = requestBillingReminderForError(msg, getActivityErrorCode(err));
      chatState.setErrorForTarget(initialTarget, msg, getActivityErrorCode(err));
      if (chatPathToStream) {
        chatState.pushLiveStep(chatPathToStream, {
          id: `err-${Date.now()}`,
          type: 'error',
          label: billingInfo?.summary || msg,
          status: 'error',
          ts: Date.now(),
        });
      }
      rollbackSubmittedRun(runningTarget, chatPathToStream);
      return;
    }
  }

  const userContentPayload = isContinuationSubmit
    ? null
    : buildChatUserContentPayload(submissionText);
  if (abortController.signal.aborted) {
    return;
  }
  const modelContentPayload = isContinuationSubmit
    ? null
    : buildChatUserContentPayload(submissionText);
  let activityKey = chatPathToStream || threadID;

  if (chatPathToStream && (!runningTarget || getTargetChatPath(chatState, runningTarget) !== chatPathToStream)) {
    const chatTarget = targetForChatPath(chatState, chatPathToStream);
    if (runningTarget) {
      moveTargetAbortController(workspaceTabId, runningTarget, chatTarget);
      chatState.setTargetInProgress(runningTarget, false);
    } else {
      setTargetAbortController(workspaceTabId, chatTarget, abortController);
    }
    chatState.setTargetInProgress(chatTarget, true);
    runningTarget = chatTarget;
  }

  chatState.prepareLiveOverlayForNewRun(activityKey);
  const chatCapableOpCode = ws.getAgentOpCode?.(agentID) ?? '';
  if (!chatCapableOpCode) {
    const msg = getAgentSelectionError(agentID);
    chatState.setErrorForTarget(runningTarget || initialTarget, msg);
    chatState.pushLiveStep(activityKey, {
      id: `err-${Date.now()}`,
      type: 'error',
      label: msg,
      status: 'error',
      ts: Date.now(),
    });
    rollbackSubmittedRun(runningTarget, chatPathToStream);
    return;
  }

  const executionModelMeta = resolveExecutionModelPreferenceMeta(effectiveModel);

  let streamEndedPath = '';
  let streamAborted = false;
  let streamCompleted = false;
  let optimisticUserPreviewPath = '';
  let selectedSkillContext: SelectedSkillContext | null = null;
  let planSkillRequested = false;
  let hadPlanPathBefore = false;
  let threadMetaForPlan: ThreadMeta | null = null;
  let planDirForTurn = '';
  let existingPlanPathForTurn = '';

  try {
    let threadMeta = freshThreadMeta ?? threadOnlyThreadMeta ?? await resolveRuntimeThreadMeta(workspaceTabId, {
      chatPath: chatPathToStream,
      ...(threadID ? { threadID } : {}),
      ...(agentID ? { agentID } : {}),
    });
    chatPathToStream = normalizePosixPath((threadMeta.chatPath || threadMeta.path || '').trim());
    threadID = threadMeta.threadID;
    activityKey = chatPathToStream || threadID;
    const threadTarget = targetForThreadMeta(threadMeta);
    if (runningTarget && getConversationTargetKey(runningTarget) !== getConversationTargetKey(threadTarget)) {
      moveTargetAbortController(workspaceTabId, runningTarget, threadTarget);
      chatState.setTargetInProgress(runningTarget, false);
    } else if (!runningTarget) {
      setTargetAbortController(workspaceTabId, threadTarget, abortController);
    }
    runningTarget = threadTarget;
    chatState.setTargetInProgress(runningTarget, true);
    if (chatPathToStream && !isContinuationSubmit && !freshThreadMeta) {
      const nextAutoTitle = buildSubmissionTitleSeed(rawText, selectedSkill);
      const autoRetitleTitle = resolveAutoRetitleTitle(threadMeta, nextAutoTitle);
      if (autoRetitleTitle) {
        const previousChatPath = chatPathToStream;
        threadMeta = await retitleThread({
          chatPath: chatPathToStream,
          threadID,
          fileID: threadMeta.fileID,
          title: autoRetitleTitle,
        }, workspaceTabId);
        chatPathToStream = threadMeta.chatPath;
        threadID = threadMeta.threadID;
        if (previousChatPath !== chatPathToStream && runningTarget?.kind === 'thread') {
          const retitledTarget = targetForThreadMeta(threadMeta);
          moveTargetAbortController(
            workspaceTabId,
            runningTarget,
            retitledTarget,
          );
          runningTarget = retitledTarget;
          chatState.requestChatScrollToBottom(chatPathToStream);
        }
      }
    }
    planSkillRequested = !isContinuationSubmit && isPlanBackedSkill(selectedSkill);
    if (planSkillRequested) {
      threadMetaForPlan = threadMeta;
      existingPlanPathForTurn = await resolveExistingPlanPathForThread(workspaceTabId, threadMetaForPlan);
      planDirForTurn = getParentDir(existingPlanPathForTurn)
        || getPlanDirForCwd(threadMetaForPlan.cwd || agentCwd || getChatWorkdir(chatPathToStream));
      if (isExecutePlanSkill(selectedSkill) && !existingPlanPathForTurn) {
        throw new Error('Execute Plan requires a bound plan file.');
      }
      if (!existingPlanPathForTurn && !planDirForTurn) {
        throw new Error('Plan turn requires a usable plan directory.');
      }
      await ensureBoundPlanDirectoryForThread(workspaceTabId, planDirForTurn);
      hadPlanPathBefore = Boolean(existingPlanPathForTurn);
    }
    selectedSkillContext = isContinuationSubmit
      ? null
      : resolveSelectedSkillContext(selectedSkill, {
        planPath: existingPlanPathForTurn || threadMetaForPlan?.planPath || '',
        planDir: planDirForTurn,
        title: threadMetaForPlan?.title || threadMeta.title,
      });
    if (planSkillRequested) {
      console.info('[planTurn] start', {
        chatPath: chatPathToStream,
        threadID: threadMetaForPlan?.threadID || threadID,
        selectedSkillContext,
        existingPlanPath: existingPlanPathForTurn || null,
        planDir: planDirForTurn || null,
      });
    }

    if (chatPathToStream && !isContinuationSubmit && userContentPayload) {
      const authProfile = useAuthStore.getState().profile;
      const previewChunk = buildChatUserTranscriptChunk(userContentPayload, {
        userID: authProfile?.uid || useAuthStore.getState().uid || null,
      });
      if (appendOptimisticUserTranscriptChunk(workspaceTabId, chatPathToStream, previewChunk)) {
        optimisticUserPreviewPath = chatPathToStream;
        chatState.requestChatScrollToBottom(chatPathToStream);
      }
    }

    const payload = {
      meta: {
        ...executionModelMeta,
        ...buildSelectedSkillMeta(selectedSkill, selectedSkillContext),
        cwd: threadMeta.cwd || agentCwd,
        modelKey: effectiveModelKey,
        threadID,
        fileID: threadMeta.fileID,
        path: chatPathToStream,
        chatPath: chatPathToStream,
        agentID,
        opcode: THREAD_SUBMIT_OPCODE,
        ...(chatState.gbrainQueryScope ? { gbrainQueryScope: chatState.gbrainQueryScope } : {}),
        ...(planSkillRequested ? { planTurn: true } : {}),
        agentName,
        agentAvatar,
      },
      ...(isContinuationSubmit || !modelContentPayload ? {} : { content: modelContentPayload }),
    };

    const turnRequestID = generateTurnRequestID();
    await streamChatWithReconnect({
      url: `${resolveChatBaseUrl(workspaceTabId)}/v1/chat/stream`,
      payload: {
        ...payload,
        meta: {
          ...payload.meta,
          turnRequestID,
        },
      },
      signal: abortController.signal,
      onEvent: (event) => {
        const endedPath = handleSSEEvent(workspaceTabId, event, activityKey);
        if (endedPath) {
          streamEndedPath = endedPath;
          streamCompleted = true;
        }
      },
      onReconnectStateChange: (state) => {
        if (state) {
          chatState.setReconnectState(activityKey, state.attempt, state.limit, state.message);
        } else {
          chatState.clearReconnectState(activityKey);
        }
      },
    });
  } catch (err) {
    if (isSignalAbortError(err, abortController.signal) || isContextCanceledError(err)) {
      streamAborted = true;
      return;
    }
    if (isLoginRequiredError(err)) {
      requestLogin('chat');
      restoreSubmittedDraft(runningTarget);
      return;
    }
    const msg = err instanceof Error ? err.message : (isContinuationSubmit ? 'Continue thread failed' : 'Chat request failed');
    const runningTargetKey = getConversationTargetKey(runningTarget);
    if (!isContinuationSubmit && (runningTargetKey || initialTargetKey)) {
      restoreSubmittedDraft(runningTargetKey ? runningTarget : initialTarget);
    }
    const billingInfo = requestBillingReminderForError(msg, getActivityErrorCode(err));
    chatState.setErrorForTarget(runningTarget || initialTarget, msg, getActivityErrorCode(err));
    chatState.pushLiveStep(activityKey, {
      id: `err-${Date.now()}`,
      type: 'error',
      label: billingInfo?.summary || msg,
      status: 'error',
      ts: Date.now(),
    });
  } finally {
    const finishedKey = streamEndedPath || activityKey;
    const finishedChatPath = chatPathToStream;
    if (streamAborted && finishedKey) {
      finishAbortedActivityForChatPath(chatState, workspaceTabId, finishedKey);
    }
    const finishedTarget = finishedChatPath
      ? targetForChatPath(chatState, finishedChatPath)
      : runningTarget;
    const controllerKey = controllerKeyForTarget(workspaceTabId, finishedTarget);
    if (controllerKey && abortControllers.get(controllerKey) === abortController) {
      abortControllers.delete(controllerKey);
    }
    chatState.setTargetInProgress(finishedTarget, false);

    if (!streamAborted) {
      if (finishedChatPath) {
        clearScheduledChatReload(workspaceTabId, finishedChatPath);
        await wsStore.getState().reloadOpenTabsByPaths([finishedChatPath], { skipDirty: true });
        await refreshThreadReviews(workspaceTabId, finishedChatPath);
        await refreshThreadStateBestEffort(workspaceTabId, finishedChatPath);
        if (streamCompleted && planSkillRequested) {
          try {
            const currentThreadMeta = threadMetaForPlan
              ? await getRuntimeThreadMeta(
                buildThreadLookupByPath(finishedChatPath, workspaceTabId, {
                  threadID: threadMetaForPlan.threadID,
                  fileID: threadMetaForPlan.fileID,
                }),
                workspaceTabId,
              ).catch(() => threadMetaForPlan)
              : await getRuntimeThreadMeta(buildThreadLookupByPath(finishedChatPath, workspaceTabId), workspaceTabId).catch(() => null);
            if (isPlanSkill(selectedSkill)) {
              const existingError = (chatState.getLiveOverlay(finishedChatPath).errorMessage || '').trim();
              const resolvedPlan = existingError
                ? { ok: false as const, error: existingError }
                : await (async () => {
                  const resolvedPlanPath = currentThreadMeta
                    ? await resolveUsablePlanPathForThread(workspaceTabId, currentThreadMeta)
                    : '';
                  return resolvedPlanPath
                    ? resolveBoundPlanFileForTurn(workspaceTabId, { planPath: resolvedPlanPath })
                    : { ok: false as const, error: 'server 未返回有效的 planPath。' };
                })();
              if (resolvedPlan.ok && currentThreadMeta) {
                console.info('[planTurn] success', {
                  chatPath: finishedChatPath,
                  threadID: currentThreadMeta.threadID,
                  selectedSkillContext,
                  planPath: resolvedPlan.plan.path,
                });
                if (hadPlanPathBefore) {
                  await appendPlanStatusToChat(workspaceTabId, finishedChatPath, 'updated', resolvedPlan.plan);
                } else {
                  await appendPlanStatusToChat(workspaceTabId, finishedChatPath, 'created', resolvedPlan.plan);
                }
                chatState.setErrorForChatPath(finishedChatPath, null);
                chatState.showComposer();
                chatState.setActivityExpanded(finishedChatPath, true);
                chatState.bumpPlanRevision(finishedChatPath);
                await wsStore.getState().openFile(
                  currentThreadMeta.planPath || resolvedPlan.plan.path,
                  { focusEditor: true },
                );
              } else {
                const failureReason = resolvedPlan.ok
                  ? 'Plan turn 缺少可写回的对话会话元数据。'
                  : resolvedPlan.error;
                const failureMessage = existingError || `server 未返回有效的 planPath：${failureReason}`;
                console.info('[planTurn] failure', {
                  chatPath: finishedChatPath,
                  threadID: currentThreadMeta?.threadID || threadMetaForPlan?.threadID || '',
                  selectedSkillContext,
                  failureReason,
                });
                chatState.setErrorForChatPath(finishedChatPath, failureMessage);
                await appendPlanStatusToChat(
                  workspaceTabId,
                  finishedChatPath,
                  'failed',
                  null,
                  failureMessage,
                );
              }
            } else if (isExecutePlanSkill(selectedSkill)) {
              if (!currentThreadMeta) {
                throw new Error('Execute Plan 缺少可写回的对话会话元数据。');
              }
              if (!existingPlanPathForTurn) {
                throw new Error('Execute Plan requires a bound plan file.');
              }
              chatState.setErrorForChatPath(finishedChatPath, null);
              chatState.showComposer();
              chatState.setActivityExpanded(finishedChatPath, true);
              chatState.bumpPlanRevision(finishedChatPath);
              await wsStore.getState().openFile(existingPlanPathForTurn, { focusEditor: true });
            }
          } catch (error) {
            chatState.setErrorForChatPath(finishedChatPath, error instanceof Error ? error.message : 'Plan post-processing failed');
          }
        }
      } else if (threadID) {
        await refreshThreadStateByThreadID(threadID, {
          modelKey: effectiveModelKey,
        }, workspaceTabId).catch(() => null);
      }
    } else if (chatPathToStream) {
      if (optimisticUserPreviewPath) {
        clearScheduledChatReload(workspaceTabId, optimisticUserPreviewPath);
        await wsStore.getState().reloadOpenTabsByPaths([optimisticUserPreviewPath], { skipDirty: true });
      }
      await refreshThreadReviews(workspaceTabId, chatPathToStream);
    }
  }
}

export type PlanBuildRequest = {
  chatPath: string;
  planPath: string;
  planTitle: string;
  agentID: string;
  agentName: string | null;
  agentCwd: string;
  modelKey: string | null;
  thinkingLevel: ThinkingLevel;
};

function normalizePlanBuildRequest(request: PlanBuildRequest): PlanBuildRequest {
  return {
    chatPath: normalizePosixPath(request.chatPath),
    planPath: normalizePosixPath(request.planPath),
    planTitle: (request.planTitle || '').trim(),
    agentID: (request.agentID || '').trim(),
    agentName: (request.agentName || '').trim() || null,
    agentCwd: normalizePosixPath(request.agentCwd),
    modelKey: (request.modelKey || '').trim() || null,
    thinkingLevel: request.thinkingLevel,
  };
}

function resolvePlanExecutionSkill(workspaceTabId: string): SelectedSkill {
  const skill = resolveSkillBySlug(workspaceTabId, 'execute-plan');
  if (!skill) {
    throw new Error('Execute Plan skill is unavailable.');
  }
  return skill;
}

