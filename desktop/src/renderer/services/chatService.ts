function findToolResultStep(
  steps: LiveStep[],
  toolCallID: string,
  name: string,
): LiveStep | undefined {
  if (toolCallID) {
    const exact = steps.find((step) => step.toolCall?.id === toolCallID || step.id === toolCallID);
    if (exact) {
      return exact;
    }
  }
  const toolSteps = steps.filter((step) => step.type === 'toolcall');
  const normalizedName = name.trim();
  if (normalizedName) {
    const runningNameMatch = toolSteps.find((step) => step.status === 'running' && step.label === normalizedName);
    if (runningNameMatch) {
      return runningNameMatch;
    }
  }
  return toolSteps.find((step) => step.status === 'running');
}

