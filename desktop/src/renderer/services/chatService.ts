function mergeToolCallPayload(
  existing: LiveStep['toolCall'] | undefined,
  patch: ToolCallPayload | null,
  fallbackName: string,
  fallbackRawArguments?: string,
): LiveStep['toolCall'] {
  const id = (patch?.id || existing?.id || '').trim();
  const name = (patch?.name || existing?.name || fallbackName || '').trim();
  const appendedRawArguments = patch?.delta ? `${existing?.rawArguments || ''}${patch.delta}` : '';
  const rawArguments = patch?.rawArguments
    || appendedRawArguments
    || fallbackRawArguments
    || '';
  const args = patch?.arguments || existing?.arguments;
  return {
    ...(existing || {}),
    ...(id ? { id } : {}),
    ...(name ? { name } : {}),
    ...(rawArguments ? { rawArguments } : {}),
    ...(args ? { arguments: args } : {}),
    ...(patch?.complete === true || existing?.complete === true ? { complete: true } : {}),
  };
}

