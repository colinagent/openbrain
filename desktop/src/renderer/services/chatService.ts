function resolveSkillSlug(
  meta: Record<string, unknown>,
  cwd: string | null | undefined,
  uri: string | null | undefined,
): string {
  const rawSlug = typeof meta.slug === 'string' ? meta.slug.trim() : '';
  if (rawSlug) {
    return rawSlug;
  }
  const normalizedCwd = (cwd || '').trim().replace(/\/+$/, '');
  if (normalizedCwd) {
    const parts = normalizedCwd.split('/').filter(Boolean);
    if (parts.length > 0) {
      return parts[parts.length - 1];
    }
  }
  const normalizedUri = (uri || '').trim();
  if (normalizedUri.startsWith('file://')) {
    const decoded = normalizedUri.slice('file://'.length).replace(/\/+$/, '');
    const parts = decoded.split('/').filter(Boolean);
    if (parts.length >= 2) {
      return parts[parts.length - 2];
    }
  }
  return '';
}

function resolveSkillBySlug(workspaceTabId: string, slug: string): SelectedSkill | null {
  const normalizedSlug = (slug || '').trim().toLowerCase();
  if (!normalizedSlug) {
    return null;
  }
  const nodes = useAppStore.getStoreByTabId(workspaceTabId).getState().skillNodes;
  for (const node of nodes) {
    if (((node.kind || '').trim().toLowerCase()) !== 'skill') {
      continue;
    }
    const meta = ((node.meta as Record<string, unknown> | undefined) || {});
    const nodeSlug = resolveSkillSlug(meta, node.cwd, node.uri).toLowerCase();
    if (nodeSlug !== normalizedSlug) {
      continue;
    }
    const name = (typeof meta.name === 'string' ? meta.name : '').trim() || nodeSlug;
    return {
      id: (node.id || '').trim(),
      slug: nodeSlug,
      name,
    };
  }
  return null;
}

function resolveSkillByID(workspaceTabId: string, id: string | null | undefined): SelectedSkill | null {
  const normalizedID = (id || '').trim();
  if (!normalizedID) {
    return null;
  }
  const nodes = useAppStore.getStoreByTabId(workspaceTabId).getState().skillNodes;
  for (const node of nodes) {
    if (((node.kind || '').trim().toLowerCase()) !== 'skill') {
      continue;
    }
    if ((node.id || '').trim() !== normalizedID) {
      continue;
    }
    const meta = ((node.meta as Record<string, unknown> | undefined) || {});
    const slug = resolveSkillSlug(meta, node.cwd, node.uri) || normalizedID;
    const name = (typeof meta.name === 'string' ? meta.name : '').trim() || slug;
    return {
      id: normalizedID,
      slug,
      name,
    };
  }
  return {
    id: normalizedID,
    slug: normalizedID,
    name: normalizedID,
  };
}

async function resolveUsablePlanPathForThread(
  workspaceTabId: string,
  threadMeta: ThreadMeta | null | undefined,
): Promise<string> {
  const planPath = normalizePosixPath((threadMeta?.planPath || '').trim());
  if (!planPath) {
    return '';
  }
  const wsStore = useAppStore.getStoreByTabId(workspaceTabId);
  const stat = await wsStore.getState().statPath(planPath);
  if (stat.error || stat.isDir) {
    return '';
  }
  const content = await wsStore.getState().readTextFile(planPath);
  if (content == null) {
    return '';
  }
  return planPath;
}

