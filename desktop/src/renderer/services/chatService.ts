function targetForThreadStateKey(
  chatState: ChatWorkspaceState,
  stateKey: string | null | undefined,
): ConversationTarget {
  const normalizedKey = normalizePosixPath((stateKey || '').trim());
  if (!normalizedKey) {
    return null;
  }
  const threadMeta = chatState.getThreadMeta(normalizedKey)
    || chatState.getThreadMetaByThreadID(normalizedKey);
  if (threadMeta?.threadID) {
    return targetForThreadMeta(threadMeta);
  }
  const indexedThreadID = (chatState.pathToThreadID[normalizedKey] || '').trim();
  if (indexedThreadID) {
    return threadTarget(indexedThreadID, normalizedKey);
  }
  return looksLikeChatPathKey(normalizedKey)
    ? targetForChatPath(chatState, normalizedKey)
    : threadTarget(normalizedKey, null);
}

