export async function compactCurrentThread(): Promise<void> {
  const workspaceTabId = getActiveWorkspaceTabId();
  const chatState = getWorkspaceChatState(workspaceTabId);
  const selectedTarget = chatState.selectedConversationTarget;
  if (!selectedTarget || selectedTarget.kind !== 'thread') {
    chatState.setErrorForTarget(selectedTarget, 'Select a chat thread to compact.');
    return;
  }
  const stateKey = getTargetChatPath(chatState, selectedTarget) || selectedTarget.threadID.trim();
  if (!stateKey) {
    chatState.setErrorForTarget(selectedTarget, 'Select a chat thread to compact.');
    return;
  }
  if (!requireSignedIn('compact')) {
    return;
  }
  if (!(await ensureChatAccessOrPrompt('compact'))) {
    return;
  }

  try {
    const wasRunning = chatState.isTargetInProgress(selectedTarget);
    const context = await resolveThreadControlContext(workspaceTabId, chatState, stateKey);
    const threadMeta = context.threadMeta;
    const compactTarget = context.target;
    const ws = useAppStore.getStoreByTabId(workspaceTabId).getState();
    const selectedModelKey = chatState.getSelectedModelKey();
    const resolvedAgent = ws.resolveAgentByID?.((threadMeta.agentID || '').trim());
    await ensureChatModelsLoaded();
    const { effectiveModelKey, effectiveModel, errorMessage } = resolveExecutionModelConfig(
      selectedModelKey,
      resolvedAgent?.model || null,
    );
    if (!effectiveModelKey || errorMessage) {
      throw new Error(errorMessage || getNoModelAvailableMessage());
    }
    if (!await ensureCurrentModelAccess({
      effectiveModelKey,
      effectiveModel,
      loginReason: 'compact',
      onDenied: (message) => chatState.setErrorForTarget(compactTarget, message),
    })) {
      return;
    }
    const executionModelMeta = resolveExecutionModelPreferenceMeta(effectiveModel);
    chatState.clearLiveOverlay(context.stateKey);
    chatState.setActivityExpanded(context.stateKey, false);
    chatState.setTargetInProgress(compactTarget, true);
    const compactStartID = `compact-${Date.now()}`;
    chatState.pushLiveStep(context.stateKey, {
      id: compactStartID,
      type: 'notice',
      label: 'Compacting',
      status: 'running',
      detail: 'Compacting older context into a checkpoint summary…',
      ts: Date.now(),
    });
    const ack = await postChatControl(workspaceTabId, {
      meta: {
        opcode: 'thread/compact',
        threadID: threadMeta.threadID,
        fileID: threadMeta.fileID,
        path: threadMeta.path || threadMeta.chatPath,
        chatPath: threadMeta.chatPath,
        agentID: threadMeta.agentID,
        modelKey: effectiveModelKey,
        ...executionModelMeta,
      },
    });
    if (wasRunning) {
      stopTargetStream(workspaceTabId, compactTarget, context.stateKey);
    }
    await syncQueuedMessagesFromAck(workspaceTabId, context.stateKey, ack.queuedMessages);
    chatState.setErrorForTarget(compactTarget, null);
    chatState.updateLiveStep(context.stateKey, compactStartID, {
      label: 'Compacted',
      status: 'done',
      detail: 'Compacted older context into a checkpoint summary.',
    });
    chatState.setTargetInProgress(compactTarget, false);
    chatState.showComposer();
    chatState.setActivityExpanded(context.stateKey, true);
    if (context.chatPath) {
      await useAppStore.getStoreByTabId(workspaceTabId).getState().reloadOpenTabsByPaths([context.chatPath], { skipDirty: true });
      await refreshThreadStateBestEffort(workspaceTabId, context.chatPath);
    } else {
      await refreshThreadStateByThreadID(context.threadID, {}, workspaceTabId).catch(() => null);
    }
  } catch (error) {
    chatState.setTargetInProgress(selectedTarget, false);
    const message = error instanceof Error ? error.message : 'Compact failed';
    chatState.setErrorForTarget(selectedTarget, message);
    chatState.pushLiveStep(stateKey, {
      id: `compact-error-${Date.now()}`,
      type: 'error',
      label: message,
      status: 'error',
      ts: Date.now(),
    });
    throw error;
  }
}

