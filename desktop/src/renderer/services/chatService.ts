async function queueRunningChatMessage(options: QueueRunningChatMessageOptions): Promise<void> {
  const workspaceTabId = getActiveWorkspaceTabId();
  const chatState = getWorkspaceChatState(workspaceTabId);
  const selectedTarget = chatState.selectedConversationTarget;
  const rawText = chatState.getDraftForTarget(selectedTarget);
  if (!selectedTarget || selectedTarget.kind !== 'thread' || !chatState.isTargetInProgress(selectedTarget)) {
    throw new Error('No running chat to control.');
  }

  if (!requireSignedIn('thread-control')) {
    return;
  }
  if (!(await ensureChatAccessOrPrompt('thread-control'))) {
    return;
  }

  const chatPath = getTargetChatPath(chatState, selectedTarget);
  const selectedThreadID = selectedTarget.threadID.trim();
  const stateKey = chatPath || selectedThreadID;
  if (!stateKey) {
    throw new Error('No running chat to control.');
  }
  const ws = useAppStore.getStoreByTabId(workspaceTabId).getState();
  let controlThreadMeta: ThreadMeta | null = null;
  if (!chatPath && selectedThreadID) {
    controlThreadMeta = await resolveControlThreadMeta(workspaceTabId, { threadID: selectedThreadID });
  }
  const turnAgentTarget = resolveTurnAgentTarget(chatState, ws, selectedTarget, chatPath)
    || agentTargetFromThreadMeta(controlThreadMeta);
  const turnAgentID = (turnAgentTarget?.agentID || '').trim();
  const turnAgentCwd = (turnAgentTarget?.agentCwd || '').trim();
  const resolvedTurnAgent = ws.resolveAgentByID?.(turnAgentID);
  const selectedModelKey = chatState.getSelectedModelKey();
  if (!turnAgentID) {
    throw new Error(getAgentSelectionError(turnAgentID));
  }
  await ensureChatModelsLoaded();
  const { effectiveModelKey, effectiveModel, errorMessage } = resolveExecutionModelConfig(
    selectedModelKey,
    resolvedTurnAgent?.model || null,
  );
  if (!effectiveModelKey || errorMessage) {
    throw new Error(errorMessage || getNoModelAvailableMessage());
  }
  if (!await ensureCurrentModelAccess({
    effectiveModelKey,
    effectiveModel,
    loginReason: 'thread-control',
    onDenied: (message) => chatState.setErrorForTarget(selectedTarget, message),
  })) {
    return;
  }
  const executionModelMeta = resolveExecutionModelPreferenceMeta(effectiveModel);
  const selectedSkill = options.selectedSkillOverride !== undefined
    ? normalizeSelectedSkill(options.selectedSkillOverride)
    : normalizeSelectedSkill(chatState.selectedSkill);
  if (!hasTurnSubmissionContent(rawText, selectedSkill)) {
    return;
  }
  const submissionText = buildSubmissionUserText(rawText, selectedSkill);
  const optimisticMessage = buildOptimisticQueuedMessage({
    chatPath: stateKey,
    kind: options.opcode === 'thread/steer' ? 'steering' : 'follow_up',
    text: submissionText,
    selectedSkill,
    selectedSkillContext: null,
    planTurn: isPlanSkill(selectedSkill),
    agentTarget: turnAgentTarget,
  });
  chatState.appendQueuedMessage(stateKey, optimisticMessage);
  chatState.setDraftForSelectedTarget('');
  chatState.clearSelectedSkill();
  chatState.setErrorForTarget(selectedTarget, null);

  let rollbackChatPath = stateKey;
  try {
    const threadMeta = controlThreadMeta ?? await resolveControlThreadMeta(workspaceTabId, chatPath
      ? { chatPath, threadID: selectedThreadID, agentID: turnAgentID }
      : { threadID: selectedThreadID, agentID: turnAgentID });
    const threadKey = threadStateKey(threadMeta);
    rollbackChatPath = threadKey || stateKey;
    const existingPlanPath = isPlanSkill(selectedSkill)
      ? await resolveExistingPlanPathForThread(workspaceTabId, threadMeta)
      : '';
    const planDir = isPlanSkill(selectedSkill)
      ? (getParentDir(existingPlanPath) || getPlanDirForCwd(threadMeta.cwd))
      : '';
    if (isPlanSkill(selectedSkill) && !existingPlanPath && !planDir) {
      throw new Error('Plan turn requires a usable plan directory.');
    }
    const threadMetaWithPlan = threadMeta;
    if (planDir) {
      await ensureBoundPlanDirectoryForThread(workspaceTabId, planDir);
    }
    const usablePlanPath = isExecutePlanSkill(selectedSkill)
      ? await resolveUsablePlanPathForThread(workspaceTabId, threadMetaWithPlan)
      : '';
    const selectedPlanPath = existingPlanPath || usablePlanPath;
    const selectedSkillContext = resolveSelectedSkillContext(selectedSkill, {
      planPath: selectedPlanPath,
      planDir,
      title: threadMetaWithPlan.title,
    });
    removeOptimisticQueuedMessage(chatState, optimisticMessage.id, stateKey, threadKey);
    chatState.appendQueuedMessage(threadKey, {
      ...optimisticMessage,
      chatPath: threadKey,
      selectedSkillContext,
    });
    const ack = await postChatControl(workspaceTabId, {
      meta: {
        opcode: options.opcode,
        threadID: threadMetaWithPlan.threadID,
        fileID: threadMetaWithPlan.fileID,
        path: threadMetaWithPlan.path || threadMetaWithPlan.chatPath,
        chatPath: threadMetaWithPlan.chatPath,
        agentID: turnAgentID,
        cwd: threadMetaWithPlan.cwd || turnAgentCwd,
        modelKey: effectiveModelKey,
        ...executionModelMeta,
        ...(turnAgentTarget?.agentName ? { agentName: turnAgentTarget.agentName } : {}),
        ...(isPlanSkill(selectedSkill) ? { planTurn: true } : {}),
        ...buildSelectedSkillMeta(selectedSkill, selectedSkillContext),
      },
      content: buildChatUserContentPayload(submissionText),
    });
    await syncQueuedMessagesFromAck(workspaceTabId, threadKey, ack.queuedMessages);
    chatState.setErrorForTarget(selectedTarget, null);
  } catch (error) {
    removeOptimisticQueuedMessage(chatState, optimisticMessage.id, stateKey, rollbackChatPath);
    const currentDraft = chatState.getDraftForTarget(chatState.selectedConversationTarget);
    chatState.setDraftForSelectedTarget(currentDraft.trim() ? mergeDraftText(rawText, currentDraft) : rawText);
    if (!chatState.selectedSkill) {
      chatState.setSelectedSkill(selectedSkill);
    }
    if (isLoginRequiredError(error)) {
      requestLogin('thread-control');
      return;
    }
    if (isThreadNotRunningError(error)) {
      await reconcileThreadControlFailure(workspaceTabId, stateKey);
    }
    chatState.setErrorForTarget(selectedTarget, error instanceof Error ? error.message : options.fallbackError);
    throw error;
  }
}

