function snapshotDirectionForEntryWindow(
  entryWindow: ThreadSnapshotEntryWindowQuery['mode'] | null | undefined,
): ThreadSnapshotWindowMergeDirection {
  if (entryWindow === 'before') {
    return 'prepend';
  }
  if (entryWindow === 'after') {
    return 'append';
  }
  return 'tail';
}

export async function loadThreadSnapshotWindow(options: {
  chatPath?: string | null;
  threadID?: string | null;
  modelKey?: string | null;
  mode: ThreadSnapshotEntryWindowQuery['mode'];
  anchorId?: string | null;
  limit?: number;
}, workspaceTabId = getActiveWorkspaceTabId()): Promise<ThreadSnapshot | null> {
  const chatState = getWorkspaceChatState(workspaceTabId);
  const normalizedChatPath = normalizePosixPath((options.chatPath || '').trim());
  const normalizedThreadID = (options.threadID || '').trim();
  if (!normalizedChatPath && !normalizedThreadID) {
    return null;
  }

  const target = normalizedThreadID
    ? threadTarget(normalizedThreadID, normalizedChatPath)
    : targetForChatPath(chatState, normalizedChatPath);
  const modelKey = options.modelKey || chatState.getModelKeyForTarget(target) || null;
  const lookup = normalizedChatPath
    ? buildThreadLookupByPath(normalizedChatPath, workspaceTabId, { threadID: normalizedThreadID })
    : { threadID: normalizedThreadID };
  const entryWindow: ThreadSnapshotEntryWindowQuery = {
    mode: options.mode,
    ...(options.anchorId ? { anchorId: options.anchorId } : {}),
    ...(typeof options.limit === 'number' && Number.isFinite(options.limit) ? { limit: options.limit } : {}),
  };
  const snapshot = await getThreadSnapshot({
    ...lookup,
    ...(modelKey ? { modelKey } : {}),
    entryWindow,
  }, workspaceTabId);

  assertWindowedThreadSnapshot(snapshot);
  const snapshotTarget = threadTarget(snapshot.meta.threadID, snapshot.meta.chatPath || normalizedChatPath);
  const current = chatState.getThreadSnapshotForTarget(snapshotTarget);
  if (
    options.mode !== 'tail'
    && current
    && (current.revision || '').trim()
    && (snapshot.revision || '').trim()
    && (current.revision || '').trim() !== (snapshot.revision || '').trim()
  ) {
    return refreshThreadStateByThreadID(
      snapshot.meta.threadID,
      { chatPath: snapshot.meta.chatPath || normalizedChatPath, modelKey },
      workspaceTabId,
    );
  }

  const direction = snapshotDirectionForEntryWindow(snapshot.entryWindow?.mode || options.mode);
  const synced = await syncThreadSnapshotState(workspaceTabId, snapshot, { direction });
  syncThreadContextUsage(workspaceTabId, snapshot.meta.chatPath || normalizedChatPath || snapshot.meta.threadID, synced.contextUsage);
  return synced;
}

