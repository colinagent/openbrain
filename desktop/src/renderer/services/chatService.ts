  await runPreparedPlanExecution({
    workspaceTabId,
    chatPath: threadMeta.chatPath,
    planPath: normalized.planPath,
    text: buildExecutePlanPrompt(normalized.planPath),
    skill: resolvePlanExecutionSkill(workspaceTabId),
    agentTarget: {
      agentID: normalized.agentID,
      agentName: normalized.agentName,
      agentCwd: normalized.agentCwd,
      agentAvatar: resolvedAgent?.avatar || null,
      agentModelKey: resolvedAgent?.model || null,
    },
    selectedModelKey: normalized.modelKey,
    thinkingLevel: normalized.thinkingLevel,
  });
