function mergeDraftText(existing: string, nextText: string): string {
  const trimmedNext = nextText.trim();
  if (!trimmedNext) {
    return existing;
  }
  const trimmedExisting = existing.trim();
  if (!trimmedExisting) {
    return trimmedNext;
  }
  return `${trimmedExisting}\n\n${trimmedNext}`;
}

async function queuedMessageFromControlItem(
  workspaceTabId: string,
  chatPath: string,
  kind: QueuedMessageKind,
  item: ChatControlQueueItem | null | undefined,
  index: number,
): Promise<QueuedMessage | null> {
  if (!item?.message) {
    return null;
  }
  const message = item.message;
  const parts = Array.isArray(message.content_parts) ? message.content_parts : [];
  const textParts = parts
    .filter((part) => part.type === 'text' && typeof part.text === 'string')
    .map((part) => part.text!.trim())
    .filter(Boolean);
  const text = (typeof message.content === 'string' ? message.content.trim() : '') || textParts.join('\n');
  if (!text) {
    return null;
  }
  const selectedSkillIDs = Array.isArray(item.selectedSkillIDs)
    ? item.selectedSkillIDs
      .map((value) => (typeof value === 'string' ? value.trim() : ''))
      .filter(Boolean)
    : [];
  const agentID = (item.agentID || '').trim();
  const agentName = (item.agentName || '').trim();
  const agentCwd = normalizePosixPath((item.cwd || '').trim());
  return {
    chatPath: chatPath.trim(),
    id: (item.id || '').trim() || `queue-fallback-${kind}-${index}`,
    kind,
    text,
    ...(agentID ? { agentID } : {}),
    ...(agentName ? { agentName } : {}),
    ...(agentCwd ? { agentCwd } : {}),
    selectedSkill: resolveSkillByID(workspaceTabId, selectedSkillIDs[0] || ''),
    selectedSkillIDs,
    selectedSkillContext: normalizeSelectedSkillContext(item.selectedSkillContext),
    planTurn: item.planTurn === true,
  };
}

async function queuedMessagesFromAck(
  workspaceTabId: string,
  chatPath: string,
  ack?: ChatControlQueuedMessages,
): Promise<QueuedMessageBucket> {
  if (!ack) {
    return { steering: [], followUp: [] };
  }
  const steering = (
    await Promise.all(
      (Array.isArray(ack.steering) ? ack.steering : []).map((item, index) => (
        queuedMessageFromControlItem(workspaceTabId, chatPath, 'steering', item, index)
      ))
    )
  ).filter((message): message is QueuedMessage => Boolean(message));
  const followUp = (
    await Promise.all(
      (Array.isArray(ack.followUp) ? ack.followUp : []).map((item, index) => (
        queuedMessageFromControlItem(workspaceTabId, chatPath, 'follow_up', item, index)
      ))
    )
  ).filter((message): message is QueuedMessage => Boolean(message));
  return { steering, followUp };
}

async function syncQueuedMessagesFromAck(
  workspaceTabId: string,
  chatPath: string,
  ack?: ChatControlQueuedMessages,
): Promise<QueuedMessageBucket> {
  const chatState = getWorkspaceChatState(workspaceTabId);
  const queuedMessages = await queuedMessagesFromAck(workspaceTabId, chatPath, ack);
  chatState.syncQueuedMessages(chatPath, queuedMessages);
  return queuedMessages;
}

