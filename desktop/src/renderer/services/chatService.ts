async function resolveThreadControlContext(
  workspaceTabId: string,
  chatState: ChatWorkspaceState,
  stateKeyOrPath: string,
  options?: { agentID?: string | null },
): Promise<ThreadControlContext> {
  const normalizedKey = normalizePosixPath((stateKeyOrPath || '').trim());
  if (!normalizedKey) {
    throw new Error('Thread key is required.');
  }
  const cachedMeta = chatState.getThreadMeta(normalizedKey)
    || chatState.getThreadMetaByThreadID(normalizedKey);
  const cachedChatPath = normalizePosixPath((cachedMeta?.chatPath || cachedMeta?.path || '').trim());
  const cachedThreadID = (cachedMeta?.threadID || '').trim();
  const agentID = (options?.agentID || cachedMeta?.agentID || '').trim();
  const threadMeta = await resolveControlThreadMeta(workspaceTabId, cachedChatPath
    ? { chatPath: cachedChatPath, threadID: cachedThreadID, agentID }
    : cachedThreadID
      ? { threadID: cachedThreadID, agentID }
      : looksLikeChatPathKey(normalizedKey)
        ? { chatPath: normalizedKey, agentID }
        : { threadID: normalizedKey, agentID });
  chatState.upsertThreadMeta(threadMeta);
  const chatPath = normalizePosixPath((threadMeta.chatPath || threadMeta.path || '').trim());
  const threadID = (threadMeta.threadID || cachedThreadID || (!chatPath ? normalizedKey : '')).trim();
  if (!threadID) {
    throw new Error('Thread metadata is missing threadID.');
  }
  return {
    threadMeta,
    stateKey: chatPath || threadID,
    chatPath,
    threadID,
    target: threadTarget(threadID, chatPath),
  };
}

