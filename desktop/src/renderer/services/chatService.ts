function setTargetAbortController(
  workspaceTabId: string,
  target: ConversationTarget,
  controller: AbortController,
): void {
  const controllerKey = controllerKeyForTarget(workspaceTabId, target);
  if (!controllerKey) {
    return;
  }
  abortControllers.set(controllerKey, controller);
}

function moveTargetAbortController(
  workspaceTabId: string,
  fromTarget: ConversationTarget,
  toTarget: ConversationTarget,
): void {
  const fromKey = controllerKeyForTarget(workspaceTabId, fromTarget);
  const toKey = controllerKeyForTarget(workspaceTabId, toTarget);
  if (!fromKey || !toKey || fromKey === toKey) {
    return;
  }
  const controller = abortControllers.get(fromKey);
  if (!controller) {
    return;
  }
  abortControllers.delete(fromKey);
  abortControllers.set(toKey, controller);
}

