function clearScheduledChatReload(workspaceTabId: string, chatPath?: string | null): void {
  const trimmedPath = (chatPath || '').trim();
  if (trimmedPath) {
    const key = chatReloadKey(workspaceTabId, trimmedPath);
    const timer = pendingChatReloadTimers.get(key);
    if (timer) {
      clearTimeout(timer);
      pendingChatReloadTimers.delete(key);
    }
    return;
  }

  for (const [key, timer] of pendingChatReloadTimers.entries()) {
    if (!key.startsWith(`${workspaceTabId}:`)) {
      continue;
    }
    clearTimeout(timer);
    pendingChatReloadTimers.delete(key);
  }
}

function scheduleChatTabReload(workspaceTabId: string, chatPath: string, delayMs = 80): void {
  const trimmedPath = chatPath.trim();
  if (!trimmedPath) {
    return;
  }
  const key = chatReloadKey(workspaceTabId, trimmedPath);
  const previous = pendingChatReloadTimers.get(key);
  if (previous) {
    clearTimeout(previous);
  }
  const timer = setTimeout(() => {
    pendingChatReloadTimers.delete(key);
    void useAppStore.getStoreByTabId(workspaceTabId).getState().reloadOpenTabsByPaths([trimmedPath], { skipDirty: true });
  }, delayMs);
  pendingChatReloadTimers.set(key, timer);
}

