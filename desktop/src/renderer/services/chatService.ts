export function handleSSEEvent(
  workspaceTabId: string,
  event: GeneralContent,
  requestChatPath: string,
  options?: {
    chatState?: ChatEventState;
    ws?: ChatEventWorkspaceState;
  },
): string | null {
  const chatState = options?.chatState ?? getWorkspaceChatState(workspaceTabId);
  const ws = options?.ws ?? useAppStore.getStoreByTabId(workspaceTabId).getState();
  const typ = (event.meta?.type ?? '') as string;
  const eventChatPath = typeof event.meta?.chatPath === 'string' ? event.meta.chatPath.trim() : '';
  const activityChatPath = eventChatPath || requestChatPath;

  if (activityChatPath) {
    chatState.clearReconnectState(activityChatPath);
  }

  switch (typ) {
    case 'start':
      if (activityChatPath) {
        clearActiveReasoningStepIDsForChatPath(workspaceTabId, activityChatPath);
      }
      return null;

    case 'text_start':
      if (activityChatPath) {
        finishReasoningForChatPath(chatState, workspaceTabId, activityChatPath);
        chatState.startStreamingTextBlock(activityChatPath);
      }
      return null;

    case 'text_delta': {
      const chunk = extractText(event);
      if (chunk && activityChatPath) {
        finishReasoningForChatPath(chatState, workspaceTabId, activityChatPath);
        chatState.appendStreamingText(activityChatPath, chunk);
      }
      return null;
    }

    case 'text_end':
      return null;

    case 'thinking_start': {
      if (!activityChatPath) {
        return null;
      }
      const index = parseMetaIndex(event.meta?.contentIndex);
      finalizeRunningReasoningSteps(chatState, activityChatPath);
      const id = startReasoningStepID(workspaceTabId, activityChatPath, index);
      chatState.pushLiveStep(activityChatPath, {
        id,
        type: 'reasoning',
        label: 'Thinking',
        status: 'running',
        ts: Date.now(),
      });
      return null;
    }

    case 'thinking_delta': {
      if (!activityChatPath) {
        return null;
      }
      const index = parseMetaIndex(event.meta?.contentIndex);
      const id = getActiveReasoningStepID(workspaceTabId, activityChatPath, index)
        || startReasoningStepID(workspaceTabId, activityChatPath, index);
      const content = extractText(event) || '';
      const existing = chatState.getLiveOverlay(activityChatPath).steps.find((step) => step.id === id);
      if (existing) {
        chatState.updateLiveStep(activityChatPath, id, {
          detail: `${existing.detail || ''}${content}`,
          status: 'running',
        });
      } else {
        chatState.pushLiveStep(activityChatPath, {
          id,
          type: 'reasoning',
          label: 'Thinking',
          status: 'running',
          detail: content,
          ts: Date.now(),
        });
      }
      return null;
    }

    case 'thinking_end': {
      if (!activityChatPath) {
        return null;
      }
      const index = parseMetaIndex(event.meta?.contentIndex);
      const id = getActiveReasoningStepID(workspaceTabId, activityChatPath, index)
        || startReasoningStepID(workspaceTabId, activityChatPath, index);
      const content = extractText(event) || '';
      const existing = chatState.getLiveOverlay(activityChatPath).steps.find((step) => step.id === id);
      if (existing) {
        chatState.updateLiveStep(activityChatPath, id, {
          detail: content || existing.detail,
          status: 'done',
        });
      } else {
        chatState.pushLiveStep(activityChatPath, {
          id,
          type: 'reasoning',
          label: 'Thinking',
          status: 'done',
          detail: content,
          ts: Date.now(),
        });
      }
      clearActiveReasoningStepID(workspaceTabId, activityChatPath, index);
      return null;
    }

    case 'toolcall_start': {
      if (!activityChatPath) {
        return null;
      }
      finalizeRunningReasoningSteps(chatState, activityChatPath);
      const id = canonicalToolCallStreamKey(event);
      const payload = normalizeToolCallPayload(event);
      const name = (payload?.name || (event.meta?.name as string) || 'tool').trim() || 'tool';
      const existing = chatState.getLiveOverlay(activityChatPath).steps.find((step) => step.id === id);
      const toolCall = mergeToolCallPayload(existing?.toolCall, payload, name, payload?.rawArguments || existing?.detail);
      const detail = toolCallDetailFromState(toolCall, existing?.detail);
      if (existing) {
        chatState.updateLiveStep(activityChatPath, id, {
          label: name,
          status: 'running',
          detail,
          toolCall,
        });
      } else {
        chatState.pushLiveStep(activityChatPath, {
          id,
          type: 'toolcall',
          label: name,
          status: 'running',
          detail,
          toolCall,
          ts: Date.now(),
        });
      }
      return null;
    }

    case 'toolcall_delta': {
      if (!activityChatPath) {
        return null;
      }
      const id = canonicalToolCallStreamKey(event);
      const payload = normalizeToolCallPayload(event);
      const content = getToolCallDeltaText(event);
      const existing = chatState.getLiveOverlay(activityChatPath).steps.find((step) => step.id === id);
      if (existing) {
        const detail = content
          ? `${existing.detail || ''}${content}`
          : existing.detail;
        const toolCall = mergeToolCallPayload(existing.toolCall, payload, existing.label, detail);
        chatState.updateLiveStep(activityChatPath, id, {
          detail: toolCallDetailFromState(toolCall, detail),
          status: 'running',
          toolCall,
        });
      } else {
        const name = (payload?.name || (event.meta?.name as string) || 'tool').trim() || 'tool';
        const toolCall = mergeToolCallPayload(undefined, payload, name);
        chatState.pushLiveStep(activityChatPath, {
          id,
          type: 'toolcall',
          label: name,
          status: 'running',
          detail: toolCallDetailFromState(toolCall, content),
          toolCall,
          ts: Date.now(),
        });
      }
      return null;
    }

    case 'toolcall_end': {
      if (!activityChatPath) {
        return null;
      }
      const id = canonicalToolCallStreamKey(event);
      const payload = normalizeToolCallPayload(event);
      const content = getToolCallSnapshotRawArguments(event);
      const existing = chatState.getLiveOverlay(activityChatPath).steps.find((step) => step.id === id);
      const name = (payload?.name || (event.meta?.name as string) || existing?.label || 'tool').trim() || 'tool';
      if (existing) {
        const toolCall = mergeToolCallPayload(existing.toolCall, payload, name, content || payload?.rawArguments || existing.detail);
        chatState.updateLiveStep(activityChatPath, id, {
          detail: toolCallDetailFromState(toolCall, existing.detail),
          status: 'running',
          label: name,
          toolCall,
        });
      } else {
        const toolCall = mergeToolCallPayload(undefined, payload, name, content || payload?.rawArguments);
        chatState.pushLiveStep(activityChatPath, {
          id,
          type: 'toolcall',
          label: name,
          status: 'running',
          detail: toolCallDetailFromState(toolCall, content || payload?.rawArguments),
          toolCall,
          ts: Date.now(),
        });
      }
      return null;
    }

    case 'done': {
      if (activityChatPath) {
        finishReasoningForChatPath(chatState, workspaceTabId, activityChatPath);
      }
      return null;
    }

    case 'message': {
      const record = extractJsonPayload<MessengerRecord>(event);
      if (record?.id && record.channelID) {
        useMessengerStore.getState().upsertRecord(record);
      }
      return null;
    }

    case 'queuedMessageDelivered': {
      const itemID = typeof event.meta?.itemID === 'string' ? event.meta.itemID.trim() : '';
      if (itemID) {
        chatState.removeQueuedMessage(activityChatPath, itemID);
      }
      return null;
    }

    case 'user_step': {
      if (activityChatPath) {
        scheduleChatTabReload(workspaceTabId, activityChatPath);
        chatState.bumpPlanRevision(activityChatPath);
      }
      return null;
    }

    case 'assistant_step': {
      return null;
    }

    case 'tool_result_step': {
      const toolResult = extractJsonPayload<{
        role?: string;
        content?: string;
        tool_call_id?: string;
        name?: string;
      }>(event);
      const toolCallID = (toolResult?.tool_call_id || '').trim();
      const toolOutput = (toolResult?.content || '').trim() || undefined;
      const name = (toolResult?.name || (event.meta?.name as string) || 'tool').trim() || 'tool';
      if (activityChatPath) {
        const existing = findToolResultStep(
          chatState.getLiveOverlay(activityChatPath).steps,
          toolCallID,
          name,
        );
      if (existing) {
        chatState.updateLiveStep(activityChatPath, existing.id, {
          label: name,
          status: 'done',
          toolCall: mergeToolCallPayload(existing.toolCall, { id: toolCallID, name }, name),
          toolOutput,
        });
        } else {
          chatState.pushLiveStep(activityChatPath, {
            id: toolCallID || `tool-result-${Date.now()}`,
            type: 'toolcall',
            label: name,
            status: 'done',
            toolOutput,
            ts: Date.now(),
          });
        }
      }
      return null;
    }

    case 'turn_result': {
      const finalPath = eventChatPath || requestChatPath;
      if (finalPath) {
        scheduleChatTabReload(workspaceTabId, finalPath);
        chatState.bumpPlanRevision(finalPath);
      }
      return null;
    }

    case 'tokenUsage': {
      if (activityChatPath) {
        const inputTokens = parseTokenCount(event.meta?.loopInputTokens);
        const outputTokens = parseTokenCount(event.meta?.loopOutputTokens);
        const totalTokens = parseTokenCount(event.meta?.loopTotalTokens);
        const normalizedTotal = totalTokens > 0 ? totalTokens : inputTokens + outputTokens;
        if (normalizedTotal > 0) {
          const contextWindow = parseTokenCount(event.meta?.contextWindow);
          const contextTokens = parseTokenCount(event.meta?.contextTokens);
          const contextPercentMilli = parseTokenCount(event.meta?.contextPercentMilli);
          const contextKnown = parseMetaBoolean(event.meta?.contextKnown);
          chatState.setLiveTokenUsage(activityChatPath, {
            inputTokens,
            outputTokens,
            totalTokens: normalizedTotal,
            ...(contextTokens > 0 ? { contextTokens } : {}),
            ...(contextWindow > 0 ? { contextWindow } : {}),
            ...(contextWindow > 0 && contextKnown !== null ? { contextKnown } : {}),
            ...(contextPercentMilli > 0 ? { contextPercent: contextPercentMilli / 1000 } : {}),
          });
        }
      }
      return null;
    }

    case 'auto_retry_start': {
      if (!activityChatPath) {
        return null;
      }
      const payload = extractJsonPayload<{
        attempt?: number;
        maxAttempts?: number;
        delayMs?: number;
        errorMessage?: string;
      }>(event);
      chatState.setAutoRetryState(
        activityChatPath,
        Number(payload?.attempt || 0),
        Number(payload?.maxAttempts || 0),
        Number(payload?.delayMs || 0),
        typeof payload?.errorMessage === 'string' ? payload.errorMessage : null,
      );
      return null;
    }

    case 'auto_retry_end': {
      if (activityChatPath) {
        chatState.clearAutoRetryState(activityChatPath);
      }
      return null;
    }

    case 'error': {
      const msg = extractText(event) || 'Unknown error';
      const errorCode = typeof event.meta?.code === 'string' ? event.meta.code.trim() || null : null;
      if (isContextCanceledError(new Error(msg))) {
        if (activityChatPath) {
          finishAbortedActivityForChatPath(chatState, workspaceTabId, activityChatPath);
          chatState.setErrorForChatPath(activityChatPath, 'Aborted');
        }
        return null;
      }
      const billingInfo = requestBillingReminderForError(msg, errorCode);
      if (activityChatPath) {
        finishReasoningForChatPath(chatState, workspaceTabId, activityChatPath);
        chatState.setErrorForChatPath(activityChatPath, msg, errorCode);
      } else {
        chatState.setError(msg, errorCode);
      }
      if (activityChatPath) {
        chatState.pushLiveStep(activityChatPath, {
          id: `err-${Date.now()}`,
          type: 'error',
          label: billingInfo?.summary || msg,
          status: 'error',
          ts: Date.now(),
        });
      }
      return null;
    }

    case 'end': {
      const finalPath = eventChatPath || chatState.targetChatPath || requestChatPath;
      if (finalPath) {
        const finalPathIsFile = finalPath.includes('/');
        if (finalPathIsFile) {
          const hasOpenTab = ws.documents.some((tab) => (
            tab.documentRole === 'conversation' && (tab.filePath || '').trim() === finalPath
          ));
          if (!hasOpenTab) {
            void ws.openThreadTab(finalPath);
          }
          const selectedPath = getTargetChatPath(chatState, chatState.selectedConversationTarget);
          const isSelectedFinalPath = selectedPath === finalPath;
          if (isSelectedFinalPath) {
            chatState.selectChatConversation(finalPath);
          }
          ws.moveChatTabToLast(finalPath);
          chatState.bumpPlanRevision(finalPath);
        }
      }
      finishReasoningForChatPath(chatState, workspaceTabId, finalPath);
      return finalPath || null;
    }

    default:
      return null;
  }

  return null;
}

