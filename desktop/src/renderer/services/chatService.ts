function syncThreadContextUsage(
  workspaceTabId: string,
  chatPath: string,
  usage: ThreadSnapshot['contextUsage'] | null | undefined,
): void {
  const normalizedChatPath = normalizePosixPath((chatPath || '').trim());
  const tokens = Number(usage?.tokens || 0);
  const contextWindow = Number(usage?.contextWindow || 0);
  if (!normalizedChatPath || contextWindow <= 0) {
    return;
  }
  const percentMilli = Number(usage?.percentMilli || 0);
  const known = usage?.known === true || tokens > 0;
  if (!known) {
    getWorkspaceChatState(workspaceTabId).setLiveTokenUsage(normalizedChatPath, {
      inputTokens: 0,
      outputTokens: 0,
      totalTokens: 0,
      contextWindow,
      contextKnown: false,
    });
    return;
  }
  if (tokens <= 0) {
    return;
  }
  getWorkspaceChatState(workspaceTabId).setLiveTokenUsage(normalizedChatPath, {
    inputTokens: 0,
    outputTokens: 0,
    totalTokens: 0,
    contextTokens: tokens,
    contextWindow,
    contextKnown: true,
    ...(percentMilli > 0 ? { contextPercent: percentMilli / 1000 } : {}),
  });
}

function assertWindowedThreadSnapshot(snapshot: ThreadSnapshot): void {
  const entries = Array.isArray(snapshot.entries) ? snapshot.entries : [];
  if (!snapshot.entryWindow) {
    throw new Error('Runtime is out of date: thread snapshots must include entryWindow. Update the workspace runtime and reopen the thread.');
  }
  if (entries.length > 1000) {
    throw new Error('Runtime is out of date: thread snapshot entries exceeded the protocol window cap. Update the workspace runtime and reopen the thread.');
  }
}

async function syncThreadSnapshotState(
  workspaceTabId: string,
  snapshot: ThreadSnapshot,
  options?: { direction?: ThreadSnapshotWindowMergeDirection },
): Promise<ThreadSnapshot> {
  assertWindowedThreadSnapshot(snapshot);
  const chatState = getWorkspaceChatState(workspaceTabId);
  const chatPath = normalizePosixPath((snapshot.meta.chatPath || '').trim());
  const threadID = (snapshot.meta.threadID || '').trim();
  const stateKey = chatPath || threadID;
  if (!stateKey) {
    return snapshot;
  }
  const snapshotTarget = threadTarget(threadID, chatPath);
  const localControllerKey = controllerKeyForTarget(workspaceTabId, snapshotTarget)
    || controllerKeyForChatPath(workspaceTabId, chatPath);
  const hasLocalController = Boolean(localControllerKey && abortControllers.get(localControllerKey));
  const isRunning = snapshot.runStatus === 'running' || hasLocalController;
  const direction = options?.direction || 'tail';
  if (direction === 'tail') {
    chatState.syncThreadSnapshot(snapshot);
  } else {
    chatState.mergeThreadSnapshotWindow(snapshot, direction);
  }
  chatState.setTargetInProgress(snapshotTarget, isRunning);
  if (!isRunning) {
    chatState.clearAwaitingUser(stateKey);
  }
  chatState.patchThreadSnapshotState(stateKey, {
    runStatus: isRunning ? 'running' : (snapshot.runStatus || 'idle'),
    tailStatus: snapshot.tailStatus || 'empty',
    continuationReason: snapshot.continuationReason || '',
  });
  await syncQueuedMessagesFromAck(workspaceTabId, stateKey, snapshot.queuedMessages);
  return chatState.getThreadSnapshotForTarget(snapshotTarget) || snapshot;
}

